using System;

namespace DeclareVariables
{
    class DeclareVariables
    {
        static void Main()
        {
            ushort valueOne = 52130;
            sbyte valueTwo = -115;
            int valueThree = 4825932;
            byte valueFour = 97;
            short valueFive = -10000;
            Console.WriteLine("The lucky numbers are {0}, {1}, {2}, {3}, and {4}!", valueOne, valueTwo, valueThree, valueFour, valueFive);
        }
    }
}
